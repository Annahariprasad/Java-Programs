
public class Escape_Sequences {

	public static void main(String[] args) {
		// Escape Sequences
		
		String doubleQuote = "\" \"";
		System.out.println("This is the DoubleQuote : " + doubleQuote);
	    String singleQuote = "\' \'";
	    System.out.println("This is the SingleQuote : " + singleQuote);
	    String nextLine = "Anna\nHari";
	    System.out.println("This is the next line : " + nextLine);
	    String tab = "Anna\tHari";
	    System.out.println("This is the tab : " + tab);
	}

}
